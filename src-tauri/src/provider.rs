use std::time::Duration;

use anyhow::{anyhow, Context};
use reqwest::blocking::Client;
use serde::{Deserialize, Serialize};

/// Configuration for an AI summary provider.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct AiConfig {
    pub enabled: bool,
    pub provider: String,
    pub base_url: String,
    pub api_key: String,
    pub model: String,
}

impl Default for AiConfig {
    fn default() -> Self {
        Self {
            enabled: false,
            provider: "deepseek".to_string(),
            base_url: "https://api.deepseek.com/v1".to_string(),
            api_key: String::new(),
            model: "deepseek-chat".to_string(),
        }
    }
}

/// Result from a test_connection call.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ConnectionTestResult {
    pub success: bool,
    pub message: String,
    pub models: Vec<String>,
}

/// A summary block generated by an AI provider.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct GeneratedSummary {
    pub block_start: String,
    pub block_end: String,
    pub main_focus: String,
    pub apps_projects: Vec<String>,
    pub context_switches: usize,
    pub total_focus_minutes: i64,
    pub productivity_notes: Vec<String>,
    pub plain_english_summary: String,
    pub model_used: String,
}

/// The trait each provider must implement.
pub trait SummaryProvider: Send + Sync {
    fn test_connection(&self) -> anyhow::Result<ConnectionTestResult>;
    fn generate_summary(&self, prompt: &str) -> anyhow::Result<GeneratedSummary>;
}

// ---------------------------------------------------------------------------
// LM Studio provider
// ---------------------------------------------------------------------------

pub struct LmStudioProvider {
    client: Client,
    config: AiConfig,
}

impl LmStudioProvider {
    pub fn new(config: AiConfig) -> Self {
        let client = Client::builder()
            .timeout(Duration::from_secs(30))
            .build()
            .expect("reqwest client");
        Self { client, config }
    }
}

impl SummaryProvider for LmStudioProvider {
    fn test_connection(&self) -> anyhow::Result<ConnectionTestResult> {
        let url = format!("{}/models", self.config.base_url.trim_end_matches('/'));
        let resp = self
            .client
            .get(&url)
            .send()
            .context("LM Studio connection refused")?;
        if !resp.status().is_success() {
            return Ok(ConnectionTestResult {
                success: false,
                message: format!("HTTP {}", resp.status()),
                models: vec![],
            });
        }
        let body: serde_json::Value = resp.json().context("invalid LM Studio response")?;
        let models: Vec<_> = body["data"]
            .as_array()
            .map(|arr| {
                arr.iter()
                    .filter_map(|m| m["id"].as_str().map(|s| s.to_string()))
                    .collect()
            })
            .unwrap_or_default();
        Ok(ConnectionTestResult {
            success: !models.is_empty(),
            message: if models.is_empty() {
                "Connected but no models loaded".to_string()
            } else {
                format!("Connected — {} model(s) loaded", models.len())
            },
            models,
        })
    }

    fn generate_summary(&self, prompt: &str) -> anyhow::Result<GeneratedSummary> {
        let url = format!("{}/chat/completions", self.config.base_url.trim_end_matches('/'));
        let model = if self.config.model.is_empty() {
            "local-model"
        } else {
            &self.config.model
        };
        let body = serde_json::json!({
            "model": model,
            "messages": [
                {"role": "system", "content": "You are a productivity assistant. Analyze the activity data and return ONLY valid JSON without markdown fences or commentary."},
                {"role": "user", "content": prompt}
            ],
            "temperature": 0.3,
            "max_tokens": 1024,
        });
        let resp: serde_json::Value = self
            .client
            .post(&url)
            .json(&body)
            .send()
            .context("LM Studio request failed")?
            .json()
            .context("LM Studio response parse failed")?;
        let content = resp["choices"][0]["message"]["content"]
            .as_str()
            .ok_or_else(|| anyhow!("LM Studio returned empty response"))?;
        parse_summary_response(content, model)
    }
}

