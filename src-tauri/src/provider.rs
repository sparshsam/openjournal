// ---------------------------------------------------------------------------
// OpenAI-compatible provider (scaffold — disabled by default)
// ---------------------------------------------------------------------------

pub struct OpenAiCompatibleProvider {
    client: Client,
    config: AiConfig,
}

impl OpenAiCompatibleProvider {
    pub fn new(config: AiConfig) -> Self {
        let client = Client::builder()
            .timeout(Duration::from_secs(30))
            .build()
            .expect("reqwest client");
        Self { client, config }
    }
}

impl SummaryProvider for OpenAiCompatibleProvider {
    fn test_connection(&self) -> anyhow::Result<ConnectionTestResult> {
        let url = format!("{}/models", self.config.base_url.trim_end_matches('/'));
        let mut req = self.client.get(&url);
        if !self.config.api_key.is_empty() {
            req = req.header("Authorization", format!("Bearer {}", self.config.api_key));
        }
        let resp = req.send().context("OpenAI-compatible connection refused")?;
        if !resp.status().is_success() {
            return Ok(ConnectionTestResult {
                success: false,
                message: format!("HTTP {}", resp.status()),
                models: vec![],
            });
        }
        let body: serde_json::Value = resp.json()?;
        let models: Vec<_> = body["data"]
            .as_array()
            .map(|arr| {
                arr.iter()
                    .filter_map(|m| m["id"].as_str().map(|s| s.to_string()))
                    .collect()
            })
            .unwrap_or_default();
        Ok(ConnectionTestResult {
            success: !models.is_empty(),
            message: if models.is_empty() {
                "Connected but no models available".to_string()
            } else {
                format!("Connected — {} model(s) available", models.len())
            },
            models,
        })
    }

    fn generate_summary(&self, prompt: &str) -> anyhow::Result<GeneratedSummary> {
        let url = format!("{}/chat/completions", self.config.base_url.trim_end_matches('/'));
        let model = if self.config.model.is_empty() {
            "gpt-4o-mini"
        } else {
            &self.config.model
        };
        let mut req = self
            .client
            .post(&url)
            .json(&serde_json::json!({
                "model": model,
                "messages": [
                    {"role": "system", "content": "You are a productivity assistant. Return ONLY valid JSON without markdown fences."},
                    {"role": "user", "content": prompt}
                ],
                "temperature": 0.3,
                "max_tokens": 1024,
            }));
        if !self.config.api_key.is_empty() {
            req = req.header("Authorization", format!("Bearer {}", self.config.api_key));
        }
        let resp: serde_json::Value = req.send()?.json()?;
        let content = resp["choices"][0]["message"]["content"]
            .as_str()
            .ok_or_else(|| anyhow!("OpenAI-compatible returned empty response"))?;
        parse_summary_response(content, model)
    }
}

// ---------------------------------------------------------------------------
// Factory
// ---------------------------------------------------------------------------

pub fn create_provider(config: &AiConfig) -> anyhow::Result<Box<dyn SummaryProvider>> {
    match config.provider.as_str() {
        "lm_studio" => Ok(Box::new(LmStudioProvider::new(config.clone()))),
        "ollama" => Ok(Box::new(OllamaProvider::new(config.clone()))),
        "deepseek" | "openai_compatible" => Ok(Box::new(OpenAiCompatibleProvider::new(config.clone()))),
        other => Err(anyhow!("Unknown provider: {other}")),
    }
}

// ---------------------------------------------------------------------------
// Response parser
// ---------------------------------------------------------------------------

fn parse_summary_response(content: &str, model: &str) -> anyhow::Result<GeneratedSummary> {
    // Strip markdown fences if present
    let cleaned = content
        .trim()
        .strip_prefix("```json")
        .or_else(|| content.trim().strip_prefix("```"))
        .and_then(|s| s.strip_suffix("```"))
        .map(|s| s.trim())
        .unwrap_or(content.trim());

    let val: serde_json::Value =
        serde_json::from_str(cleaned).context("AI returned invalid JSON — check model output")?;

    Ok(GeneratedSummary {
        block_start: val["block_start"].as_str().unwrap_or("").to_string(),
        block_end: val["block_end"].as_str().unwrap_or("").to_string(),
        main_focus: val["main_focus"].as_str().unwrap_or("Work session").to_string(),
        apps_projects: val["apps_projects"]
            .as_array()
            .map(|a| a.iter().filter_map(|v| v.as_str().map(|s| s.to_string())).collect())
            .unwrap_or_default(),
        context_switches: val["context_switches"].as_u64().unwrap_or(0) as usize,
        total_focus_minutes: val["total_focus_minutes"].as_i64().unwrap_or(0),
        productivity_notes: val["productivity_notes"]
            .as_array()
            .map(|a| a.iter().filter_map(|v| v.as_str().map(|s| s.to_string())).collect())
            .unwrap_or_default(),
        plain_english_summary: val["plain_english_summary"]
            .as_str()
            .unwrap_or("No summary generated.")
            .to_string(),
        model_used: model.to_string(),
    })
}
